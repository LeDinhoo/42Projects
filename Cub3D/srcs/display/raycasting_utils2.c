#include "../../includes/cub3D.h"

float	degtorad(float a)
{
	return (a * PI / 180.0);
}

float	fixang(float a)
{
	if (a > 359)
	{
		a -= 360;
	}
	if (a < 0)
	{
		a += 360;
	}
	return (a);
}
